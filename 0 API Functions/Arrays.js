// Cheat Sheet

// Functional programming style
// Readability :thumbs-up: vs Performance :thumbs-down:

const array = [];

// JAVASCRIPT vs LINQ:
array.filter(); // WHERE
array.map(); // SELECT
array.includes(); // CONTAINS
array.some(); // ANY

// LOOPS:
array.forEach(); // Foreach Loop

// OTHER:
array.reduce(); // [] => single value
array.pop(); // Remove last index and returns
array.splice(); // add/remove items
array.slice(); // Selects elements and returns new array
